    @Test
    public void batchWrite_withSupportedAndUnsupportedSchema_shouldPreserveValues() {
        Map<String, AttributeValue> customItem = new HashMap<>();
        customItem.put("pk", AttributeValue.builder().s("custom-1").build());
        customItem.put("details",
                       AttributeValue.builder().m(Collections.singletonMap("inner",
                                                                           AttributeValue.builder().s("v").build()))
                                     .build());

        BatchWriteItemEnhancedRequest request =
            BatchWriteItemEnhancedRequest.builder()
                                         .writeBatches(
                                             WriteBatch.builder(TimestampedRecord.class)
                                                       .mappedTableResource(timestampedTable)
                                                       .addPutItem(r -> r.item(new TimestampedRecord().setId("ts-1")))
                                                       .build(),
                                             WriteBatch.builder(customTableType())
                                                       .mappedTableResource(unsupportedCustomSchemaTable)
                                                       .addPutItem(r -> r.item(customItem))
                                                       .build())
                                         .build();

        extensionEnhancedClient.batchWriteItem(request);

        TimestampedRecord persistedTimestamped = timestampedTable.getItem(r -> r.key(k -> k.partitionValue("ts-1")));
        Map<String, AttributeValue> persistedCustom =
            unsupportedCustomSchemaTable.getItem(r -> r.key(k -> k.partitionValue("custom-1")));

        assertThat(persistedTimestamped.getTime(), is(notNullValue()));
        assertThat(persistedCustom.get("details").m().get("inner").s(), is("v"));
    }

