package software.amazon.awssdk.enhanced.dynamodb.functionaltests.extensions;

import static org.assertj.core.api.Assertions.assertThat;
import static software.amazon.awssdk.enhanced.dynamodb.functionaltests.AutoGeneratedUuidRecordTest.assertValidUuid;

import java.time.Instant;
import java.util.Objects;
import java.util.stream.Collectors;
import java.util.stream.Stream;
import org.junit.After;
import org.junit.Before;
import org.junit.Test;
import software.amazon.awssdk.enhanced.dynamodb.DynamoDbEnhancedClient;
import software.amazon.awssdk.enhanced.dynamodb.DynamoDbTable;
import software.amazon.awssdk.enhanced.dynamodb.TableSchema;
import software.amazon.awssdk.enhanced.dynamodb.extensions.AutoGeneratedTimestampRecordExtension;
import software.amazon.awssdk.enhanced.dynamodb.extensions.AutoGeneratedUuidExtension;
import software.amazon.awssdk.enhanced.dynamodb.extensions.annotations.DynamoDbAutoGeneratedTimestampAttribute;
import software.amazon.awssdk.enhanced.dynamodb.extensions.annotations.DynamoDbAutoGeneratedUuid;
import software.amazon.awssdk.enhanced.dynamodb.extensions.annotations.DynamoDbVersionAttribute;
import software.amazon.awssdk.enhanced.dynamodb.functionaltests.LocalDynamoDbSyncTestBase;
import software.amazon.awssdk.enhanced.dynamodb.internal.client.ExtensionResolver;
import software.amazon.awssdk.enhanced.dynamodb.mapper.annotations.DynamoDbBean;
import software.amazon.awssdk.enhanced.dynamodb.mapper.annotations.DynamoDbPartitionKey;

public class ChainExtensionTest extends LocalDynamoDbSyncTestBase {

    private static final TableSchema<MultiExtensionRecord> TABLE_SCHEMA =
        TableSchema.fromClass(MultiExtensionRecord.class);

    private final DynamoDbEnhancedClient enhancedClient = DynamoDbEnhancedClient
        .builder()
        .dynamoDbClient(getDynamoDbClient())
        .extensions(Stream.concat(
                              ExtensionResolver.defaultExtensions().stream(),
                              Stream.of(
                                  AutoGeneratedUuidExtension.create(),
                                  AutoGeneratedTimestampRecordExtension.create()))
                          .collect(Collectors.toList()))
        .build();

    private final DynamoDbTable<MultiExtensionRecord> mappedTable =
        enhancedClient.table(getConcreteTableName("chain-extension-table"), TABLE_SCHEMA);

    @Before
    public void createTable() {
        mappedTable.createTable(r -> r.provisionedThroughput(getDefaultProvisionedThroughput()));
    }

    @After
    public void deleteTable() {
        getDynamoDbClient().deleteTable(r -> r.tableName(getConcreteTableName("chain-extension-table")));
    }

    @Test
    public void putItem_appliesAllExtensions() {
        Instant beforePut = Instant.now();
        MultiExtensionRecord record = new MultiExtensionRecord();
        record.setData("data");
        mappedTable.putItem(record);

        Instant afterPut = Instant.now();
        MultiExtensionRecord retrieved = mappedTable.scan().items().stream().findFirst().orElse(null);
        assertThat(retrieved).isNotNull();

        // UUID extension
        assertValidUuid(retrieved.getId());

        // Timestamp extension
        assertThat(retrieved.getCreatedAt()).isNotNull();
        assertThat(retrieved.getCreatedAt()).isAfter(beforePut);
        assertThat(retrieved.getCreatedAt()).isBefore(afterPut);

        // Version extension
        assertThat(retrieved.getVersion()).isEqualTo(1L);
    }

    @DynamoDbBean
    public static class MultiExtensionRecord {
        private String id;
        private String data;
        private Instant createdAt;
        private Long version;

        @DynamoDbPartitionKey
        @DynamoDbAutoGeneratedUuid
        public String getId() {
            return id;
        }

        public void setId(String id) {
            this.id = id;
        }

        public String getData() {
            return data;
        }

        public void setData(String data) {
            this.data = data;
        }

        @DynamoDbAutoGeneratedTimestampAttribute
        public Instant getCreatedAt() {
            return createdAt;
        }

        public void setCreatedAt(Instant createdAt) {
            this.createdAt = createdAt;
        }

        @DynamoDbVersionAttribute
        public Long getVersion() {
            return version;
        }

        public void setVersion(Long version) {
            this.version = version;
        }

        @Override
        public boolean equals(Object o) {
            if (this == o) {
                return true;
            }
            if (o == null || getClass() != o.getClass()) {
                return false;
            }
            MultiExtensionRecord that = (MultiExtensionRecord) o;
            return Objects.equals(id, that.id) &&
                   Objects.equals(data, that.data) &&
                   Objects.equals(createdAt, that.createdAt) &&
                   Objects.equals(version, that.version);
        }

        @Override
        public int hashCode() {
            return Objects.hash(id, data, createdAt, version);
        }
    }
}
