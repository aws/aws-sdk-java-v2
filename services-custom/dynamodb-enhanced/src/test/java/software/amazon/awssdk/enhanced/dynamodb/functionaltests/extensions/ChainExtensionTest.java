public class ChainExtensionTest extends LocalDynamoDbSyncTestBase {

    private static final String TABLE_NAME = "table-name";
    private static final String ITEM_ID = "test-id";
    private static final String ITEM_DATA_CREATED = "created";
    private static final String ITEM_DATA_UPDATED = "updated";

    private static final TableSchema<MultiExtensionRecord> TABLE_SCHEMA =
        TableSchema.fromClass(MultiExtensionRecord.class);

    private final DynamoDbEnhancedClient enhancedClient = DynamoDbEnhancedClient
        .builder()
        .dynamoDbClient(getDynamoDbClient())
        .extensions(Stream.concat(
                              ExtensionResolver.defaultExtensions().stream(),
                              Stream.of(
                                  AutoGeneratedUuidExtension.create(),
                                  AutoGeneratedTimestampRecordExtension.create()))
                          .collect(Collectors.toList()))
        .build();

    private final DynamoDbTable<MultiExtensionRecord> mappedTable =
        enhancedClient.table(getConcreteTableName(TABLE_NAME), TABLE_SCHEMA);

    @Before
    public void createTable() {
        mappedTable.createTable(r -> r.provisionedThroughput(getDefaultProvisionedThroughput()));
    }

    @After
    public void deleteTable() {
        getDynamoDbClient().deleteTable(r -> r.tableName(getConcreteTableName(TABLE_NAME)));
    }

    @Test
    public void transactWriteItem_putItemThenUpdateItemAndGetItem_appliesAllChainedExtensions() {
        MultiExtensionRecord record = new MultiExtensionRecord();
        record.setId(ITEM_ID);
        record.setData(ITEM_DATA_CREATED);

        Instant beforePut = Instant.now();
        enhancedClient.transactWriteItems(TransactWriteItemsEnhancedRequest.builder()
                                                                           .addPutItem(mappedTable, record)
                                                                           .build());
        Instant afterPut = Instant.now();

        MultiExtensionRecord resultAfterPut = scanTableAndFindFirstItem();

        assertRecord(resultAfterPut,
                     ITEM_DATA_CREATED,
                     beforePut,
                     afterPut,
                     beforePut,
                     afterPut,
                     0L,
                     10L,
                     1L);

        resultAfterPut.setData(ITEM_DATA_UPDATED);

        Instant beforeUpdate = Instant.now();
        enhancedClient.transactWriteItems(TransactWriteItemsEnhancedRequest.builder()
                                                                           .addUpdateItem(mappedTable, resultAfterPut)
                                                                           .build());
        Instant afterUpdate = Instant.now();

        MultiExtensionRecord resultAfterUpdate = scanTableAndFindFirstItem();

        assertRecord(resultAfterUpdate,
                     ITEM_DATA_UPDATED,
                     beforePut,
                     afterPut,
                     beforeUpdate,
                     afterUpdate,
                     1L,
                     15L,
                     2L);
    }

    @Test
    public void putItemThenUpdateItemAndGetItem_appliesAllChainedExtensions() {
        MultiExtensionRecord record = new MultiExtensionRecord();
        record.setId(ITEM_ID);
        record.setData(ITEM_DATA_CREATED);

        Instant beforePut = Instant.now();
        mappedTable.putItem(record);
        Instant afterPut = Instant.now();

        MultiExtensionRecord resultAfterPut = scanTableAndFindFirstItem();

        assertRecord(resultAfterPut,
                     ITEM_DATA_CREATED,
                     beforePut,
                     afterPut,
                     beforePut,
                     afterPut,
                     0L,
                     10L,
                     1L);

        resultAfterPut.setData(ITEM_DATA_UPDATED);
        Instant beforeUpdate = Instant.now();
        mappedTable.updateItem(resultAfterPut);
        Instant afterUpdate = Instant.now();

        MultiExtensionRecord resultAfterUpdate = scanTableAndFindFirstItem();

        assertRecord(resultAfterUpdate,
                     ITEM_DATA_UPDATED,
                     beforePut,
                     afterPut,
                     beforeUpdate,
                     afterUpdate,
                     1L,
                     15L,
                     2L);
    }

    @Test
    public void batchWriteItem_putTwoItems_appliesAllChainedExtensionsWithoutVersion() {
        DynamoDbEnhancedClient enhancedClientWithoutVersionExtension = DynamoDbEnhancedClient
            .builder()
            .dynamoDbClient(getDynamoDbClient())
            .extensions(
                AtomicCounterExtension.builder().build(),
                AutoGeneratedUuidExtension.create(),
                AutoGeneratedTimestampRecordExtension.create())
            .build();

        DynamoDbTable<MultiExtensionRecord> mappedTableWithoutVersionExtension =
            enhancedClientWithoutVersionExtension.table(getConcreteTableName(TABLE_NAME), TABLE_SCHEMA);

        MultiExtensionRecord record1 = new MultiExtensionRecord();
        record1.setId(ITEM_ID + "-1");
        record1.setData(ITEM_DATA_CREATED);

        MultiExtensionRecord record2 = new MultiExtensionRecord();
        record2.setId(ITEM_ID + "-2");
        record2.setData(ITEM_DATA_CREATED);

        Instant beforePut = Instant.now();
        enhancedClientWithoutVersionExtension.batchWriteItem(req -> req.addWriteBatch(
            WriteBatch.builder(MultiExtensionRecord.class)
                      .mappedTableResource(mappedTableWithoutVersionExtension)
                      .addPutItem(record1)
                      .addPutItem(record2)
                      .build()));
        Instant afterPut = Instant.now();

        List<MultiExtensionRecord> results = mappedTableWithoutVersionExtension.scan().items().stream().collect(Collectors.toList());
        assertThat(results).hasSize(2);

        results.forEach(r -> {
            assertRecord(r,
                         ITEM_DATA_CREATED,
                         beforePut,
                         afterPut,
                         beforePut,
                         afterPut,
                         0L,
                         10L,
                         null);
        });
    }

    @Test
    public void batchWriteItem_putTwoItemsAndAppliesAllChainedExtensions_throwsIllegalArgumentException() {
        MultiExtensionRecord record1 = new MultiExtensionRecord();
        record1.setId(ITEM_ID + "-1");
        record1.setData(ITEM_DATA_CREATED);

        MultiExtensionRecord record2 = new MultiExtensionRecord();
        record2.setId(ITEM_ID + "-2");
        record2.setData(ITEM_DATA_CREATED);

        assertThatThrownBy(() -> enhancedClient.batchWriteItem(req -> req.addWriteBatch(
            WriteBatch.builder(MultiExtensionRecord.class)
                      .mappedTableResource(mappedTable)
                      .addPutItem(record1)
                      .addPutItem(record2)
                      .build())))
            .isInstanceOf(IllegalArgumentException.class)
            .hasMessage("A mapper extension inserted a conditionExpression in a PutItem request as part of a "
                        + "BatchWriteItemRequest. This is not supported by DynamoDb. An extension known to do "
                        + "this is the VersionedRecordExtension which is loaded by default unless overridden. "
                        + "To fix this use a table schema that does not have a versioned attribute in it or do "
                        + "not load the offending extension.");
    }

    private void assertRecord(MultiExtensionRecord result,
                              String expectedData,
                              Instant expectedCreatedAtNotBefore,
                              Instant expectedCreatedAtNotAfter,
                              Instant expectedUpdatedAtNotBefore,
                              Instant expectedUpdatedAtNotAfter,
                              Long expectedDefaultCounter,
                              Long expectedCustomCounter,
                              Long expectedVersion) {
        assertThat(result).isNotNull();
        assertThat(result.getData()).isEqualTo(expectedData);

        assertValidUuid(result.getAutogeneratedUuidWriteIfNotExists());
        assertValidUuid(result.getAutogeneratedUuidWriteAlways());

        assertThat(result.getCreatedAt()).isNotNull();
        assertThat(result.getCreatedAt()).isAfter(expectedCreatedAtNotBefore);
        assertThat(result.getCreatedAt()).isBefore(expectedCreatedAtNotAfter);

        assertThat(result.getUpdatedAt()).isNotNull();
        assertThat(result.getUpdatedAt()).isAfter(expectedUpdatedAtNotBefore);
        assertThat(result.getUpdatedAt()).isBefore(expectedUpdatedAtNotAfter);

        assertThat(result.getDefaultCounter()).isEqualTo(expectedDefaultCounter);
        assertThat(result.getCustomCounter()).isEqualTo(expectedCustomCounter);

        assertThat(result.getVersion()).isEqualTo(expectedVersion);
    }

    private MultiExtensionRecord scanTableAndFindFirstItem() {
        return mappedTable.scan().items().stream().findFirst().orElse(null);
    }

    @DynamoDbBean
    public static class MultiExtensionRecord {
        private String id;
        private String autogeneratedUuidWriteIfNotExists;
        private String autogeneratedUuidWriteAlways;
        private String data;
        private Instant createdAt;
        private Instant updatedAt;
        private Long defaultCounter;
        private Long customCounter;
        private Long version;

        @DynamoDbPartitionKey
        public String getId() {
            return id;
        }

        public void setId(String id) {
            this.id = id;
        }

        @DynamoDbAutoGeneratedUuid
        @DynamoDbUpdateBehavior(UpdateBehavior.WRITE_IF_NOT_EXISTS)
        public String getAutogeneratedUuidWriteIfNotExists() {
            return autogeneratedUuidWriteIfNotExists;
        }

        public void setAutogeneratedUuidWriteIfNotExists(String autogeneratedUuidWriteIfNotExists) {
            this.autogeneratedUuidWriteIfNotExists = autogeneratedUuidWriteIfNotExists;
        }

        @DynamoDbAutoGeneratedUuid
        @DynamoDbUpdateBehavior(UpdateBehavior.WRITE_ALWAYS)
        public String getAutogeneratedUuidWriteAlways() {
            return autogeneratedUuidWriteAlways;
        }

        public void setAutogeneratedUuidWriteAlways(String autogeneratedUuidWriteAlways) {
            this.autogeneratedUuidWriteAlways = autogeneratedUuidWriteAlways;
        }

        public String getData() {
            return data;
        }

        public void setData(String data) {
            this.data = data;
        }

        @DynamoDbAutoGeneratedTimestampAttribute
        @DynamoDbUpdateBehavior(UpdateBehavior.WRITE_IF_NOT_EXISTS)
        public Instant getCreatedAt() {
            return createdAt;
        }

        public void setCreatedAt(Instant createdAt) {
            this.createdAt = createdAt;
        }

        @DynamoDbAutoGeneratedTimestampAttribute
        @DynamoDbUpdateBehavior(UpdateBehavior.WRITE_ALWAYS)
        public Instant getUpdatedAt() {
            return updatedAt;
        }

        public void setUpdatedAt(Instant updatedAt) {
            this.updatedAt = updatedAt;
        }

        @DynamoDbAtomicCounter
        public Long getDefaultCounter() {
            return defaultCounter;
        }

        public void setDefaultCounter(Long defaultCounter) {
            this.defaultCounter = defaultCounter;
        }

        @DynamoDbAtomicCounter(delta = 5, startValue = 10)
        public Long getCustomCounter() {
            return customCounter;
        }

        public void setCustomCounter(Long customCounter) {
            this.customCounter = customCounter;
        }

        @DynamoDbVersionAttribute
        public Long getVersion() {
            return version;
        }

        public void setVersion(Long version) {
            this.version = version;
        }

        @Override
        public boolean equals(Object o) {
            if (o == null || getClass() != o.getClass()) {
                return false;
            }
            MultiExtensionRecord that = (MultiExtensionRecord) o;
            return Objects.equals(id, that.id)
                   && Objects.equals(autogeneratedUuidWriteIfNotExists, that.autogeneratedUuidWriteIfNotExists)
                   && Objects.equals(autogeneratedUuidWriteAlways, that.autogeneratedUuidWriteAlways)
                   && Objects.equals(data, that.data) && Objects.equals(createdAt, that.createdAt)
                   && Objects.equals(updatedAt, that.updatedAt)
                   && Objects.equals(defaultCounter, that.defaultCounter)
                   && Objects.equals(customCounter, that.customCounter)
                   && Objects.equals(version, that.version);
        }

        @Override
        public int hashCode() {
            return Objects.hash(id, autogeneratedUuidWriteIfNotExists, autogeneratedUuidWriteAlways, data, createdAt,
                                updatedAt, defaultCounter, customCounter, version);
        }
    }
}
